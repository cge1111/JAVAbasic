package ch8_class;

class Car {
    // 클래스 변수
    public String name;
    public String num;
    public int width;
    public int height;
    public int lenght;
    public double tank;
    public double x;
    public double y;
    public double fuel;
    public double mpg;

    // 생성자
    Car(String name, String num, int width, int height, int lenght, double tank, double mpg) {
        this.name = name;
        this.num = num;
        this.width  = width;
        this.height = height;
        this.lenght = lenght;
        this.tank = tank;
        this. mpg = mpg;

    }
}
